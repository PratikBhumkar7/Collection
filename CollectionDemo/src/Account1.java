
public class Account1 {
	private int accNo;
	private String custName;
	private double accBal;
	public Account1(int accNo, String custName, double accBal) {
		super();
		this.accNo = accNo;
		this.custName = custName;
		this.accBal = accBal;
	}
	public int getAccNo() {
		return accNo;
	}
	public String getCustName() {
		return custName;
	}
	public double getAccBal() {
		return accBal;
	}
}


